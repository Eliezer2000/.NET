        bool n = 10 < 5;
        Console.WriteLine(n);

        int num = 10;
        num += 2;
        Console.WriteLine(num);

        bool n1 = (5 < 3) | (10 > 5);
        Console.WriteLine(n1);

        bool n2 = (5 > 3) & (10 > 5);
        Console.WriteLine(n2);
    }
